namespace MightyFX.Data
{
    /// <summary>
    /// The type of values returned by a tag.
    /// </summary>
    public enum TagType
    {
        /// <summary>
        /// Returns a double floating-point value.
        /// </summary>
        Numeric,

        /// <summary>
        /// Returns a boolean true/false value.
        /// </summary>
        Boolean,

        /// <summary>
        /// Returns a string value.
        /// </summary>
        String
    }
}